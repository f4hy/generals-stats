export function TeamColor(name: string) {
  switch (name) {
    case "1":
    case "Modus":
    case "Brendan":
    case "jbb":
    case "Jared":
      return "#82ca9d"
    case "3":
    case "OneThree111":
    case "Bill":
    case "Ye_Ole_Seans":
    case "Sean":
      return "#8884d8"
    default:
      return "#444444"
  }
}
